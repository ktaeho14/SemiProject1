DROP TABLE ANIMB;

DROP SEQUENCE ANINOSEQ;

CREATE SEQUENCE ANINOSEQ NOCACHE;
CREATE TABLE ANIMB(
	MYNO NUMBER NOT NULL,
	MYID VARCHAR2(500) NOT NULL,
	MYPW VARCHAR2(500) NOT NULL,
	MYNAME VARCHAR2(1000) NOT NULL,
	MYADDR VARCHAR2(4000) NOT NULL,
	MYPHONE VARCHAR2(20) NOT NULL,
	MYEMAIL VARCHAR2(100) NOT NULL,
	MYROLE VARCHAR2(50) NOT NULL,
	CONSTRAINT ANIMB_PK PRIMARY KEY(MYNO),
	CONSTRAINT ANIMB_UQ_ID UNIQUE(MYID),
	CONSTRAINT ANIMB_UQ_PHONE UNIQUE(MYPHONE),
	CONSTRAINT ANIMB_UQ_EMAIL UNIQUE(MYEMAIL)
);

